export class EmployeeModel {

    public id!: string;
    public name!: string;
    public last_name!: string;
    public birthday!: string;
    public group!: number;

    constructor() {
        this.id = this.id,
        this.name = this.name,
        this.last_name = this.last_name,
        this.birthday = this.birthday,
        this.group = this.group
    }
}